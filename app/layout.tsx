// iOS home-screen launch images — without these a PWA cold launch flashes a
// blank screen. Generated by scripts/make-icons.py (public/splash/); one
// entry per (device points × pixel ratio) the group's iPhones use.
const SPLASH_SCREENS = [
  { w: 375, h: 812, r: 3 },
  { w: 390, h: 844, r: 3 },
  { w: 393, h: 852, r: 3 },
  { w: 402, h: 874, r: 3 },
  { w: 414, h: 896, r: 2 },
  { w: 428, h: 926, r: 3 },
  { w: 430, h: 932, r: 3 },
  { w: 440, h: 956, r: 3 },
];

export default async function RootLayout({
  children,
}: Readonly<{ children: React.ReactNode }>) {
  const data = await loadPriceData();
  const lastDate = data.tradingDates[data.tradingDates.length - 1];
  return (
    <html lang="en" className="h-full">
      <head>
        {SPLASH_SCREENS.map(({ w, h, r }) => (
          <link
            key={`${w}x${h}@${r}`}
            rel="apple-touch-startup-image"
            media={`(device-width: ${w}px) and (device-height: ${h}px) and (-webkit-device-pixel-ratio: ${r}) and (orientation: portrait)`}
            href={`/splash/splash-${w * r}x${h * r}.png`}
          />
        ))}
      </head>
      <body className="min-h-full bg-page text-ink antialiased">
        <ThemeController />
        <ServiceWorkerRegistrar />
        <InstallHint />
        <PullToRefresh />
        <main
          className="max-w-md mx-auto pb-20"
          style={{ paddingTop: "env(safe-area-inset-top)" }}
        >
          {children}
          <Footer lastDate={lastDate} generatedAt={data.generatedAt} />
        </main>
        <TabBar />
      </body>
    </html>
  );
}
